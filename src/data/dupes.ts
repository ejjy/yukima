export const findDupes = (productName: string, category?: string): Dupe[] => {
  const searchTerm = productName.toLowerCase();
  
  return dupes.filter(dupe => {
    const matchesProduct = dupe.originalProduct.toLowerCase().includes(searchTerm) ||
                          dupe.dupeName.toLowerCase().includes(searchTerm);
    const matchesCategory = !category || dupe.category.toLowerCase() === category.toLowerCase();
    
    return matchesProduct && matchesCategory;
  });
};

export const getDupesByCategory = (category: string): Dupe[] => {
  return dupes.filter(dupe => dupe.category.toLowerCase() === category.toLowerCase());
};

export const getBestSavings = (): Dupe[] => {
  return dupes
    .filter(dupe => dupe.savings > 0)
    .sort((a, b) => b.savings - a.savings)
    .slice(0, 10);
};